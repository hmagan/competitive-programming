#include <bits/stdc++.h>

using namespace std;

int main()
{
	ios::sync_with_stdio(0);
	cin.tie(0); cout.tie(0);
	
	long long n, m;
	cin >> n >> m;
		
	if(n == 1){
		cout << "1\n";
		return 0;
	}

	if(n - m + 1 <= m){
		cout << m - 1 << "\n";
	} else {
		cout << m + 1 << "\n";		
	}
}
