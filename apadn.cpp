#include <bits/stdc++.h>

using namespace std;

int prime_factors(long long n){
	int ans = 0;
	if(n % 2 == 0){
		ans += n / (n / 2) - 1;
		n /= div;
	}

	for(int i = 3; i <= sqrt(n); i+=2){
		while(n % i == 0){
			ans++;
			n /= i;
		}
	}

	if(n > 2){
		ans++;
	}

	return ans;
}

int main()
{
	ios::sync_with_stdio(0);
	cin.tie(0); cout.tie(0);
	
	int t;
	cin >> t;

	while(t--){
		long long a, b, k;
		cin >> a >> b >> k;
		
		long long GCD = gcd(a, b);
		int MAX = prime_factors(a) + prime_factors(b);
		int MIN;
		if(a == b){
			MIN = 0;
		} else if(a % b == 0 || b % a == 0){
			MIN = 1;
		} else {
			MIN = 2;
		}

		if(k == 1){
			if(MIN == 1){
				cout << "YES\n";
			} else {
				cout << "NO\n";
			}
			continue;
		}

		cout << ((MIN <= k && k <= MAX) ? "YES\n" : "NO\n");
	}	
}
