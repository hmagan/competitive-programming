#include <bits/stdc++.h>

using namespace std;

long long MOD = 1e9 + 7;

int main()
{
	ios::sync_with_stdio(0);
	cin.tie(0); cout.tie(0);
	
	long long x, y, n;
	cin >> x >> y >> n;

	n--;
	vector<long long> pattern;
	pattern.push_back(x);
	pattern.push_back(y);
	int i = 1;
	while(pattern.size() < 6){
		pattern.push_back((pattern[i] - pattern[i-1]) % MOD);
		i++;
	}
	n %= 6;
	long long ans = ((pattern[n] < 0) ? MOD + pattern[n] : pattern[n] % MOD);
	cout << ans << "\n";
}
