#include <bits/stdc++.h>

using namespace std;

int main()
{
	ios::sync_with_stdio(0);
	cin.tie(0); cout.tie(0);

	string a, b;
	cin >> a >> b;

	for(int i = 0; i < a.length(); i++){
		int one = a[i];
		int two = b[i];
		if(one < 97){
			one += 32;
		}
		if(two < 97){
			two += 32;
		}
		if(one < two){
			cout << "-1\n";
			return 0;
		} else if(two < one){
			cout << "1\n";
			return 0;
		}
	}

	cout << "0\n";
}
